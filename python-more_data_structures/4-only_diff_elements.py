#!/usr/bin/python3
"""
L'opérateur ^ est utilisé pour trouver la différence symétrique entre deux ensembles.
Cela renvoie un nouvel ensemble contenant les éléments
qui sont dans
un seul des deux ensembles (pas dans les deux à la fois).
"""
def only_diff_elements(set_1, set_2):
    return set_1 ^ set_2
